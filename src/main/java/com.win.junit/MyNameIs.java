package com.win.junit;
public class MyNameIs {
    public static void main(String[] args){
        String first = "My name is";
        String second = "Mark";
        String third = "Schledzewski";

        System.out.println(firstLine(first));
        System.out.println(secondLine(second));
        System.out.println(thirdLine(third));
    }
    public static String firstLine(String first){
        return first;
    }
    public static String secondLine(String second){
        return second;
    }
    public static String thirdLine(String third){
        return third;
    }
}